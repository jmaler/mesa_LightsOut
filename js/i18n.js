    function init() {
        const preferred = Mesa.user.getLanguages();
        currentLang = 'en';
        for (const lang of preferred) {
            if (SUPPORTED_LANGUAGES.includes(lang)) {
                currentLang = lang;
                break;
            }
        }
        document.documentElement.lang = currentLang;
        if (isRTL()) {
            document.documentElement.dir = 'rtl';
        } else {
            document.documentElement.removeAttribute('dir');
        }
        applyToDOM();
    }

    function t(key, params) {
        const langTable = translations[currentLang] || translations.en;
        let str = langTable[key] || translations.en[key] || key;
        if (!params) return str;
        return str.replace(/\{\{(\w+)\}\}/g, function(match, name) {
            return params[name] !== undefined ? String(params[name]) : match;
        });
    }

    function applyToDOM() {
        document.querySelectorAll('[data-i18n]').forEach(function(el) {
            el.textContent = t(el.getAttribute('data-i18n'));
        });
        document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
            el.innerHTML = t(el.getAttribute('data-i18n-html'));
        });
        document.querySelectorAll('[data-i18n-aria]').forEach(function(el) {
            el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
        });
    }

    function getLang() { return currentLang; }
    function isRTL() { return RTL_LANGUAGES.includes(currentLang); }

    return { init, t, applyToDOM, getLang, isRTL };
